export const COLOR_THEME_MAP = Object.fromEntries(
  COLOR_THEMES.map((t) => [t.id, t]),
) as Record<ColorTheme['id'], ColorTheme>;

export function getThemeTokens(id: ColorTheme['id'], isDark: boolean): ThemeTokens {
  const theme = COLOR_THEME_MAP[id] ?? COLOR_THEME_MAP.daylight;
  return isDark ? theme.dark : theme.light;
}

