import type { ColorTheme, ThemeTokens } from './types';

function rgba(hex: string, alpha: number): string {
  const h = hex.replace('#', '');
  const r = parseInt(h.slice(0, 2), 16);
  const g = parseInt(h.slice(2, 4), 16);
  const b = parseInt(h.slice(4, 6), 16);
  return `rgba(${r}, ${g}, ${b}, ${alpha})`;
}

function buildLightTokens(
  swatches: [string, string, string, string],
  signature: string,
  signatureDeep: string,
  signatureSoft: string,
): ThemeTokens {
  // [textPrimary, textMuted, accent, surfaceBase]
  const [textDark, textMuted, , surfaceLight] = swatches;
  return {
    surfaceBase: surfaceLight,
    surfaceRaised: '#FFFFFF',
    surfaceMuted: signatureSoft,
    textPrimary: textDark,
    accentWarm: signature,
    accentCream: surfaceLight,
    accentSage: textMuted,
    accentDepth: textDark,
    cream: surfaceLight,
    cream2: signatureSoft,
    paper: surfaceLight,
    ink: textDark,
    ink2: textMuted,
    inkSoft: textMuted,
    inkFaint: textMuted,
    bruno: signature,
    brunoDeep: signatureDeep,
    brunoLight: signature,
    brunoInk: textDark,
    belly: signatureSoft,
    honey: signature,
    honeyDeep: signatureDeep,
    honeySoft: signatureSoft,
    sage: textMuted,
    sageSoft: signatureSoft,
    line: rgba(textDark, 0.1),
    lineStrong: rgba(textDark, 0.18),
    glassBg: rgba(surfaceLight, 0.72),
    glassBorder: rgba(textDark, 0.1),
    glassShadow: `0 8px 32px ${rgba(textDark, 0.08)}`,
    glassCardBg: rgba('#FFFFFF', 0.85),
    settingsBg: surfaceLight,
    settingsCard: '#FFFFFF',
    settingsCardHover: signatureSoft,
    settingsBorder: rgba(textDark, 0.12),
    settingsText: textDark,
    settingsTextMuted: textMuted,
    settingsBrand: signature,
    sidebarBg: textDark,
    sidebarText: surfaceLight,
    focus: signatureDeep,
    focusSoft: signatureSoft,
  };
}

function buildDarkTokens(
  swatches: [string, string, string, string],
  signature: string,
  signatureDeep: string,
  signatureSoft: string,
  cardHover?: string,
  mutedText?: string,
): ThemeTokens {
  // [surfaceBase, surfaceRaised, accentOrNeutral, textPrimary]
  const [surfaceDark, surfaceRaised, third, textLight] = swatches;
  const hover = cardHover ?? surfaceRaised;
  const textMuted = mutedText ?? third;
  return {
    surfaceBase: surfaceDark,
    surfaceRaised,
    surfaceMuted: textMuted,
    textPrimary: textLight,
    accentWarm: signature,
    accentCream: surfaceDark,
    accentSage: textMuted,
    accentDepth: surfaceDark,
    cream: surfaceDark,
    cream2: surfaceRaised,
    paper: surfaceDark,
    ink: textLight,
    ink2: textLight,
    inkSoft: textMuted,
    inkFaint: textMuted,
    bruno: signature,
    brunoDeep: signatureDeep,
    brunoLight: signature,
    brunoInk: textLight,
    belly: textMuted,
    honey: signature,
    honeyDeep: signatureDeep,
    honeySoft: signatureSoft,
    sage: textMuted,
    sageSoft: signatureSoft,
    line: rgba(textLight, 0.1),
    lineStrong: rgba(textLight, 0.2),
    glassBg: rgba(surfaceRaised, 0.55),
    glassBorder: rgba(textLight, 0.12),
    glassShadow: '0 8px 32px rgba(0, 0, 0, 0.35)',
    glassCardBg: rgba(surfaceRaised, 0.75),
    settingsBg: surfaceDark,
    settingsCard: surfaceRaised,
    settingsCardHover: hover,
    settingsBorder: rgba(textLight, 0.12),
    settingsText: textLight,
    settingsTextMuted: textMuted,
    settingsBrand: signature,
    sidebarBg: surfaceDark,
    sidebarText: textLight,
    focus: signatureDeep,
    focusSoft: signatureSoft,
  };
}

