/**
 * Regenerates Bruno thinking illustration assets from the Gemini source export.
 *
 * Usage (from apps/web):
 *   node scripts/regenerate-bruno-thinking-svg.mjs
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SOURCE = path.join(
  ROOT,
  'public/Gemini_Generated_Image_5ruxsv5ruxsv5rux.svg',
);
const OUT_SVG = path.join(ROOT, 'public/bruno-thinking-empty.svg');
const OUT_TS = path.join(ROOT, 'components/bruno/bruno-thinking-art.ts');

const LIGHT_FILLS = new Set(['#FDFDFD', '#E9E8E8', '#FFFFFF']);

function bounds(d) {
  const nums = d.match(/-?[\d.]+/g)?.map(Number) ?? [];
  let minX = Infinity;
  let maxX = -Infinity;
  let minY = Infinity;
  let maxY = -Infinity;

  for (let i = 0; i < nums.length - 1; i += 2) {
    minX = Math.min(minX, nums[i]);
    maxX = Math.max(maxX, nums[i]);
    minY = Math.min(minY, nums[i + 1]);
    maxY = Math.max(maxY, nums[i + 1]);
  }

  return { minX, maxX, minY, maxY, w: maxX - minX, h: maxY - minY };
}

function isCharacterLightDetail(fill, d) {
  if (!LIGHT_FILLS.has(fill)) {
    return false;
  }

  const b = bounds(d);

  return (
    b.minX >= 280 &&
    b.maxX <= 660 &&
    b.minY >= 200 &&
    b.maxY <= 860 &&
    b.w <= 100 &&
    b.h <= 100
  );
}

function shouldDropPath(fill, d) {
  // Full-canvas trace-export frame.
  if (fill === '#522E1B' && d.startsWith('M0 0') && d.length > 500) {
    return true;
  }

  // Light fills are the transparency-checker background tiles, except for the
  // small highlights that sit on Bruno's face/paws (eye whites, sweater specular).
  if (LIGHT_FILLS.has(fill)) {
    return !isCharacterLightDetail(fill, d);
  }

  // Every remaining colored path belongs to Bruno or the chair — keep it all
  // so the legs, paws, and lower chair are not cropped away.
  return false;
}

function normalizeFill(fill) {
  if (fill === '#FDFDFD') {
    return '#FFFFFF';
  }
  return fill;
}

function extractGradients(svg) {
  const gradients = new Map();

  for (const match of svg.matchAll(
    /<linearGradient id="([^"]+)"[\s\S]*?<\/linearGradient>/g,
  )) {
    gradients.set(match[1], match[0]);
  }

  return gradients;
}

function main() {
  const svg = fs.readFileSync(SOURCE, 'utf8');
  const allGradients = extractGradients(svg);

  const paths = [...svg.matchAll(/<path fill="([^"]+)" d="([^"]+)"/g)].map(
    ([, fill, d]) => ({ fill, d }),
  );

  const kept = paths
    .filter(({ fill, d }) => !shouldDropPath(fill, d))
    .map(({ fill, d }) => ({ fill: normalizeFill(fill), d }));

  let minX = Infinity;
  let maxX = -Infinity;
  let minY = Infinity;
  let maxY = -Infinity;

  for (const { d } of kept) {
    const b = bounds(d);
    minX = Math.min(minX, b.minX);
    maxX = Math.max(maxX, b.maxX);
    minY = Math.min(minY, b.minY);
    maxY = Math.max(maxY, b.maxY);
  }

  const pad = 12;
  const viewX = Math.floor(minX - pad);
  const viewY = Math.floor(minY - pad);
  const viewW = Math.ceil(maxX - minX + pad * 2);
  const viewH = Math.ceil(maxY - minY + pad * 2);
  const viewBox = `${viewX} ${viewY} ${viewW} ${viewH}`;

  const usedGradientIds = new Set(
    kept
      .map(({ fill }) => fill.match(/^url\(#([^)]+)\)$/)?.[1])
      .filter(Boolean),
  );

  const defsMarkup = [...usedGradientIds]
    .sort()
    .map((id) => allGradients.get(id))
    .filter(Boolean)
    .join('\n');

  const pathMarkup = kept
    .map(({ fill, d }) => `  <path fill="${fill}" d="${d}"/>`)
    .join('\n');

  const svgOutput = `<?xml version="1.0" encoding="utf-8"?>
<!-- Generated by scripts/regenerate-bruno-thinking-svg.mjs — do not edit by hand. -->
<svg xmlns="http://www.w3.org/2000/svg" viewBox="${viewBox}" fill="none">
<defs>
${defsMarkup}
</defs>
${pathMarkup}
</svg>
`;

  fs.writeFileSync(OUT_SVG, svgOutput);

  const gradientEntries = [...usedGradientIds].sort().map((id) => {
    const markup = allGradients.get(id) ?? '';
    const x1 = markup.match(/x1="([^"]+)"/)?.[1] ?? '0';
    const y1 = markup.match(/y1="([^"]+)"/)?.[1] ?? '0';
    const x2 = markup.match(/x2="([^"]+)"/)?.[1] ?? '0';
    const y2 = markup.match(/y2="([^"]+)"/)?.[1] ?? '0';
    const stops = [...markup.matchAll(/<stop offset="([^"]+)" stop-color="([^"]+)"/g)].map(
      ([, offset, color]) => ({ offset, color }),
    );

    return { id, x1, y1, x2, y2, stops };
  });

  const tsOutput = `// Auto-generated by scripts/regenerate-bruno-thinking-svg.mjs — do not edit by hand.

export const BRUNO_THINKING_VIEWBOX = '${viewBox}';

export const BRUNO_THINKING_GRADIENTS = ${JSON.stringify(gradientEntries, null, 2)} as const;

export const BRUNO_THINKING_PATHS = ${JSON.stringify(kept, null, 2)} as const;
`;

  fs.writeFileSync(OUT_TS, tsOutput);

  const fillCounts = kept.reduce((acc, { fill }) => {
    acc[fill] = (acc[fill] ?? 0) + 1;
    return acc;
  }, {});

  console.log(`Wrote ${OUT_SVG}`);
  console.log(`Wrote ${OUT_TS}`);
  console.log(`Paths kept: ${kept.length} (removed ${paths.length - kept.length})`);
  console.log(`viewBox="${viewBox}"`);
  console.log(`Gradients: ${[...usedGradientIds].join(', ')}`);
  console.log('Fill counts:', fillCounts);
}

main();
